public class conditional_statements {
    public static void main(String[] args) {
        boolean isSunny = true;
        boolean isWarm = false;

        if(isSunny && isWarm){
            System.out.println("Today is beach day");
        }else if (isSunny){
            System.out.println("Apply sunscream and go to the beach.");
        }else{
            System.out.println("Ghar mein baithiye coz aaj k mousam ki c##d# padi hai");
        }
        System.out.println();
    }
    
}
